// Reference "now" = 07:30 UTC (14:30 local UTC+7) on a busy afternoon
// All times are HH:MM UTC strings
// Epoch offsets are relative so the display stays fresh

function utcHHMM(offsetMins: number): string {
  const now = new Date()
  const t = new Date(now.getTime() + offsetMins * 60000)
  return t.toISOString().substring(11, 16)
}

function msFromNow(offsetMins: number): number {
  return Date.now() + offsetMins * 60000
}

